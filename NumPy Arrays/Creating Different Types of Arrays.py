import numpy as np

ar = np.zeros(3)
print(ar)

print('###############ZEROS###############')

ar = np.zeros((3, 4) )
print(ar)

print('################ZEROS###############')

ar = np.zeros((3, 4, 5) )
print(ar)

# ############################################################
print('#############ONES###################')

ar = np.ones(3)
print(ar)

print('#############ONES###################')

ar = np.ones((3, 4))
print(ar)

print('#############ONES###################')

ar = np.ones((3, 4, 5) )
print(ar)


# ############################################################

print('#############EYES###################')

ar = np.eye(4)
print(ar)

print('#############EYES###################')

ar = np.eye(3, 4)
print(ar)

print('#############EYES###################')

ar = np.eye(3, 4, 5 )
print(ar)

# ############################################################

print('#############DIAG###################')

ar = np.diag([4])
print(ar)

print('#############DIAG###################')

ar = np.diag([2, 3])
print(ar)

print('#############DIAG###################')

ar = np.diag([3, 4, 5] )
print(ar)


# ############################################################

print('#############ARANGE###################')

ar = np.arange(0, 10)
print(ar)
ar = np.arange(0, 10, 2) # step 2
print(ar)

# ############################################################

print('#############LINESPACE###################')

ar = np.linspace(0, 10, 5)
print(ar)

ar = np.linspace(5, 12, 6)
print(ar)



