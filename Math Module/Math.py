import math

math.ceil(12.9) # 13
math.floor(13.9)  # 13
math.fabs(-5) # 5
math.fmod(11,3)  # 11/3 = 2.0
math.remainder(13.5)  # -2.0
math.sqrt(27)  # 5.196 float
math.isqrt(27)  # 5 int floor vale
math.pow(10,2) # 10 * 10 = 100
math.factorial(5)  # 1 * 2 * 3 * 4 * 5 = 120
math.gcd(35, 21)  # 7 is the greatest common divisor
math.perm(5, 2)  # 20 i.e. (npr) = n!/n-r formular  permutation of any two numbers first number
# must be larger than second number
math.comb(5,4)  # 10 (ncr) = n!/(n-r)! * r!
math.prod([1, 2, 3, 4, 5])  # 120 i.e. multiply all values together
math.fsum([1, 2, 3, 4, 5])  # 15 i.e. add all values together
math.radians()
math.degrees()
math.sin(math.radians(30))  # 0.49
math.cos(math.radians(60))  # 0.5
math.tan(math.radians(45))  # 0.99999
math.log(math.e)  # 1.0
math.log10(1000)  # 3.0
math.log2(1024)  # 10.0
math.pi #  3.1415
math.e  # 2.718
math.inf # inf
math.nan # nan
